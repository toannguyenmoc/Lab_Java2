    private void btnInputActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInputActionPerformed
        try {
            String yourName = JOptionPane.showInputDialog("Your name is");
            while (true) {
                if (yourName.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Please input your name");
                    yourName = JOptionPane.showInputDialog("Your name is");
                } else {
                    JOptionPane.showMessageDialog(this, "Your name is " + yourName);
                    break;
                }
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_btnInputActionPerformed

    private void btnComfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnComfirmActionPerformed
        int ret = JOptionPane.showConfirmDialog(this, "Are you sure", "Confirm", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);

        if (ret == JOptionPane.YES_OPTION) {
            JOptionPane.showMessageDialog(this, "Save");
        } else {
            JOptionPane.showMessageDialog(this, "Cancel");
        }
    }//GEN-LAST:event_btnComfirmActionPerformed

