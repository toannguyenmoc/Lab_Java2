    private void btnOptionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOptionActionPerformed
        int MessageType = JOptionPane.QUESTION_MESSAGE;
        String[] options = {"Java", "C++", "VB", "PHP", "Per1"};
        int code = JOptionPane.showOptionDialog(this, "What language do you prefer?", "Option Dialog Box", 0, MessageType, null, options, options[0]);
        if (code == 0) {
            JOptionPane.showMessageDialog(this, "Chào mừng đến với Java");
        } else if (code == 1) {
            JOptionPane.showMessageDialog(this, "Chào mừng đến với C++");
        } else if (code == 2) {
            JOptionPane.showMessageDialog(this, "Chào mừng đến với VB");
        } else if (code == 3) {
            JOptionPane.showMessageDialog(this, "Chào mừng đến với PHP");
        } else if (code == 4) {
            JOptionPane.showMessageDialog(this, "Chào mừng đến với Perl");
        }
    }//GEN-LAST:event_btnOptionActionPerformed

