    public boolean check() {
        String mauMaSp = "[a-zA-Z0-9]{1,5}";
        String mauDonGia = "[0-9]{1,50}";
        String mauSoLuong = "[0-9]{1,11}";
        if (txtMaSp.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập Mã Sản Phẩm");
            return false;
        }
        if (!txtMaSp.getText().matches(mauMaSp)) {
            JOptionPane.showMessageDialog(this, "Mã Sản Phẩm Không Hợp Lệ");
            txtMaSp.requestFocus();
            return false;
        }
        if (txtTenSp.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập Tên Sản Phẩm");
            return false;
        }
        if (txtDonGia.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập Đơn Giá");
            return false;
        }
        if (!txtDonGia.getText().matches(mauDonGia)) {
            JOptionPane.showMessageDialog(this, "Đơn Giá phải là số");
            txtDonGia.requestFocus();
            return false;
        }
        if (txtSoLuong.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập Số Lượng");
            return false;
        }
        if (!txtSoLuong.getText().matches(mauSoLuong)) {
            JOptionPane.showMessageDialog(this, "Số Lượng phải là số");
            txtSoLuong.requestFocus();
            return false;
        }
        return true;
    }

