    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        try {
            if (check()) {
                if (tblBang.getRowCount() > 0) {
                    for (int i = 0; i < tblBang.getRowCount(); i++) {
                        if (txtMaSp.getText().equalsIgnoreCase(tblBang.getValueAt(i, 0).toString())) {
                            JOptionPane.showMessageDialog(this, "Trùng Mã Sản Phẩm");
                            return;
                        }
                    }
                    KetNoiCSDL();
                    DecimalFormat df = new DecimalFormat("###,###");
                    String sql = "INSERT INTO SanPham VALUES(?,?,?,?,?)";
                    PreparedStatement cauLenh = ketNoi.prepareStatement(sql);
                    cauLenh.setString(1, txtMaSp.getText());
                    cauLenh.setString(2, txtTenSp.getText());
                    cauLenh.setString(3, txtDonGia.getText());
                    cauLenh.setInt(4, Integer.parseInt(txtSoLuong.getText()));
                    for (int i = 1; i <= cboLoaiSanPham.getWidth(); i++) {
                        if ((cboLoaiSanPham.getSelectedIndex() + 1) == i) {
                            cauLenh.setInt(5, i);
                        }
                    }
                    cauLenh.executeUpdate();
                    ketNoi.close();
                    DoDuLieuLenBang();
                }
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Lab_5.Bai_2.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Lab_5.Bai_2.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnThemActionPerformed

